        public static void agregarPanelNuevoAsignaturas(String asignatura , String codigo, String creditos, String tipologia,
                String periodo, String calificacion, String Estado) {
        HistoriaAcadAsigDetalle panel = new HistoriaAcadAsigDetalle();
        panel.setPreferredSize(new Dimension(910, 40));
        panel.setMaximumSize(new Dimension(910,40));
        panel.setMinimumSize(new Dimension(910,40));
        panel.setBackground(panelColors[colorIndex]);

        panel.labelMateria.setText(asignatura + " " +codigo);
        panel.labelTipo.setText(codigo);
        panel.labelCreditos.setText(creditos);
        panel.labelTipo.setText(tipologia);
        panel.labelPeriodo.setText(periodo);
        panel.labelNota.setText(calificacion);
        panel.labelAPROBADO.setText(Estado);

        colorIndex = (colorIndex + 1) % panelColors.length;
        panelList.add(panel);
        panel_Asignaturas.add(panel);
        panel_Asignaturas.revalidate();
        panel_Asignaturas.repaint();
    }      
        
